void CreateLockCursor()
{
    Pixmap source, mask;
    Colormap cmap = XDefaultColormap(display, screen);
    XColor xcolors[2];
    char noCursorBits = 0x1;
    Arg args[10];
    int i;

    /*
     * Create the padlock cursor
     */
    source = XCreateBitmapFromData(display,
				   XRootWindow(display, screen),
				   padlockBits, padlockWidth,
				   padlockHeight);
    mask = XCreateBitmapFromData(display,
				 XRootWindow(display, screen),
				 padlockmaskBits, padlockmaskWidth,
				 padlockmaskHeight);

    /* translate the Pixels into XColors */
    i = 0;
    XtSetArg(args[i], XmNtopShadowColor, &(xcolors[0]));i++;
    XtSetArg(args[i], XmNbackground, &(xcolors[1]));i++;
    XtGetValues(button, args, i);
    topShadowPix = xcolors[0].pixel;
    backgroundPix = xcolors[1].pixel;
    XQueryColors(display, cmap, xcolors, 2);

    /* create the padlock cursor */
    lockCursor = XCreatePixmapCursor(display, source, mask,
				     &(xcolors[0]), &(xcolors[1]),
				     padlockmaskXHot,
				     padlockmaskYHot);
    XFreePixmap(display, source);
    XFreePixmap(display, mask);
}

void RecolorCursor()
{
    Colormap cmap = XDefaultColormap(display, screen);
    XColor xcolors[2];

    /*
     * translate the Pixels into XColors
     */
    xcolors[0].pixel = topShadowPix;
    xcolors[1].pixel = backgroundPix;
    XQueryColors(display, cmap, xcolors, 2);

    /*
     * recolor the padlock cursor
     */
    XRecolorCursor(display, lockCursor, &(xcolors[0]),
		   &(xcolors[1]));
}
