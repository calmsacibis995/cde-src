The \*(Zx terminal emulation services are
based in part on the X Consortium's xterm and in part on a
widget architecture developed by HP.
Xterm is not wholly adopted
because it is not based on Motif, does not conform to the DT/CUA user
model, is not fully internationalised, has been announced to be mature
code by the X Consortium with no further enhancements planned for it by
that body, and contains space-consuming non-ANSI TEK 4014 graphics
terminal emulation that is not sufficiently documented or maintained by
the X Consortium, or sufficiently used by customers, to justify
inclusion in CDE.
