.H 2 "Introduction"
.mc 3
The \*(Zx terminal emulation services provide a
window for applications written for character-based terminals such as
the VT220.
These applications include XPG4 commands, shell scripts, and other
applications not written for an X Windows environment.
The services are available in two forms:
a stand-alone
client and a widget that can be used inside applications that require
terminal emulation.
.P
The \*(Zx terminal emulation services are consistent with
the \*(Z5 and the \*(Z6.
The services are based in part on the X Consortium's
.Cm xterm
and popular terminals compatible with the VT220.
.P
The user interface is consistent with the \*(ZY, so the client and widget
blend well on a screen populated by windowed applications.
