static int NumNames = sizeof(NameLUT)/sizeof(NamedColor);

static int findcolor (char *name,
           unsigned short *r,
           unsigned short *g,
           unsigned short *b)
{
        int i;
        for (i = 0; i < NumNames; i++) {
                if (strcmp(name, NameLUT[i].name)) continue;
                *r = NameLUT[i].r;
                *g = NameLUT[i].g;
                *b = NameLUT[i].b;
                return (0);
        }
        return (1);
}


int _ilefsilxpmParseColor (
        unsigned short   *pPalette,
        short   *nPaletteColors,
        char    *colorname,
        short   *index)

{
	register int n, i;
	unsigned short r, g, b;
	char c;

        if (!colorname) return(-1);
	n = strlen (colorname);
	if (*colorname != '#') {
            if (findcolor (colorname, &r, &g, &b)) return (-1);
	}
        else {
	    colorname++;
	    n--;
	    if (n != 3 && n != 6 && n != 9 && n != 12)
	        return (-1);
	    n /= 3;
	    g = b = 0;
	    do {
	        r = g;
    	        g = b;
    	        b = 0;
	        for (i = n; --i >= 0; ) {
		    c = *colorname++;
	            b <<= 4;
		    if (c >= '0' && c <= '9')
		        b |= c - '0';
		    else if (c >= 'A' && c <= 'F')
		        b |= c - ('A' - 10);
		    else if (c >= 'a' && c <= 'f')
		        b |= c - ('a' - 10);
		    else return (-1);
	        }
	    } while (*colorname != '\0');

            switch (n) {
                case 1:
                   r = r << 12 | r << 8 | r << 4 | r;
                   g = g << 12 | g << 8 | g << 4 | g;
                   b = b << 12 | b << 8 | b << 4 | b;
                   break;

                case 2:
                   r = r << 8 | r;
                   g = g << 8 | g;
                   b = b << 8 | b;
                   break; 

                case 3:
                   r = r << 4;
                   g = g << 4;
                   b = b << 4;
                   break;
            }
        }

        for (i = 0; i < *nPaletteColors; i++, pPalette++) {
            if (r == pPalette[0] &&
                g == pPalette[256] &&
                b == pPalette[512]) {
                *index = i;
                return (0);
                }
        }
        pPalette[0] = r;
        pPalette[256] = g;
        pPalette[512] = b;
        *index = (*nPaletteColors)++;
        return (0);
}
